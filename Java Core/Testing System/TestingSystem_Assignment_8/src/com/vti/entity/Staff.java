package com.vti.entity;

public class Staff<K, V> extends MyMap<K, V> {
	public Staff(K key, V value) {
		super(key, value);
	}

	@Override
	public String toString() {
		return super.toString();
	}
	
	@Override
	public K getKey() {
		return super.getKey();
	}

	@Override
	public V getValue() {
		return super.getValue();
	}
	

}
